def month_name():
    n = int(input("Введите номер месяца (1-12): "))
    months = ["январь", "февраль", "март", "апрель", "май", "июнь",
              "июль", "август", "сентябрь", "октябрь", "ноябрь", "декабрь"]
    if 1 <= n <= 12:
        print(months[n-1])
    else:
        print("Неверный номер месяца")