a2 = int(input("Введите цифру десятков двузначного числа a2: "))
a1 = int(input("Введите цифру единиц двузначного числа a1: "))
b = int(input("Введите цифру однозначного числа b: "))
a = a2 * 10 + a1
diff = a - b
diff_abs = abs(diff)
d2 = diff_abs // 10
d1 = diff_abs % 10
print(f"Цифры двузначной разности: десятки = {d2}, единицы = {d1}")
