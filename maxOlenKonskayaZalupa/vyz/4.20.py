x1_1 = float(input("x левый нижний первого прямоугольника: "))
y1_1 = float(input("y левый нижний первого прямоугольника: "))
x2_1 = float(input("x правый нижний первого прямоугольника: "))
y2_1 = float(input("y правый нижний первого прямоугольника: "))
x1_2 = float(input("x левый нижний второго прямоугольника: "))
y1_2 = float(input("y левый нижний второго прямоугольника: "))
x2_2 = float(input("x правый нижний второго прямоугольника: "))
y2_2 = float(input("y правый нижний второго прямоугольника: "))
x_min = min(x1_1, x1_2)
y_min = min(y1_1, y1_2)
x_max = max(x2_1, x2_2)
h1 = float(input("Высота первого прямоугольника: "))
h2 = float(input("Высота второго прямоугольника: "))
y_max = max(y1_1 + h1, y1_2 + h2)
print(f"Координаты минимального прямоугольника:")
print(f"Левый нижний угол: ({x_min}, {y_min})")
print(f"Правый верхний угол: ({x_max}, {y_max})")
