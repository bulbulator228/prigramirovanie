def day_of_week_k():
    k = int(input("Введите номер дня в году (1-365): "))
    if not (1 <= k <= 365):
        print("Неверный номер дня")
        return

    # a) 1 января - понедельник
    days = ["понедельник", "вторник", "среда", "четверг",
            "пятница", "суббота", "воскресенье"]
    day_a = days[(k - 1) % 7]
    print("a) День недели:", day_a)

    # б) d-й день недели 1 января (1 - понедельник, ..., 7 - воскресенье)
    d = int(input("Введите номер дня недели для 1 января (1-Пн, ..., 7-Вс): "))
    if not (1 <= d <= 7):
        print("Неверный номер дня недели для 1 января")
        return
    day_b = days[(d - 1 + k - 1) % 7]
    print("б) День недели:", day_b)
