a2 = int(input("Введите a2 (десятки первого): "))
a1 = int(input("Введите a1 (единицы первого): "))
b2 = int(input("Введите b2 (десятки второго): "))
b1 = int(input("Введите b1 (единицы второго): "))
sum_ = (a2 * 10 + a1) + (b2 * 10 + b1)
r2 = sum_ // 10
r1 = sum_ % 10
print(f"Сумма в цифрах: {r2}, {r1}")

