a2 = int(input("Введите цифру десятков первого двузначного числа a2: "))
a1 = int(input("Введите цифру единиц первого двузначного числа a1: "))
b2 = int(input("Введите цифру десятков второго двузначного числа b2: "))
b1 = int(input("Введите цифру единиц второго двузначного числа b1: "))
a = a2 * 10 + a1
b = b2 * 10 + b1
diff = a - b
diff_abs = abs(diff)
d2 = diff_abs // 10
d1 = diff_abs % 10
print(f"Цифры двузначной разности: десятки = {d2}, единицы = {d1}")